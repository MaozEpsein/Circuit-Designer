// VerilogExporter — public entry for circuitJSON → Verilog.
//
// Three-stage pipeline:
//   1. validateCircuit → HDLError[]  (warnings surfaced, errors halt export)
//   2. fromCircuit     → IRModule    (structural, width-explicit IR)
//   3. toVerilog       → string      (deterministic pretty print)
//
// Identifier sanitization is re-exported from core/identifiers.js so that
// existing callers (Phase 1 tests) continue to import it from this module.

