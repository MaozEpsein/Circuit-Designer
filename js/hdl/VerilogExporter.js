// Determinism contract: exportCircuit output is byte-identical across runs
// for a given circuitJSON. A timestamp is emitted ONLY when the caller opts
// in with `timestamp: true`; without it, round-trip (export → copy → import
// → re-export) produces identical bytes.
function buildHeaderText({ header, timestamp }) {
  if (!header) return null;
  const line1 = 'Generated by Circuit Designer — HDL Toolchain';
  if (!timestamp) return line1;
  return `${line1}\nExported: ${new Date().toISOString()}`;
}

export function exportCircuit(circuitJSON, options = {}) {
  const opts = {
    topName: 'top',
    header: true,
    timestamp: false,
    ...options,
  };

  const errors = validateCircuit(circuitJSON, { hasTranslator });
  if (hasBlockingErrors(errors) && !opts.force) {
    // Blocking errors → throw the first. Callers that want the full list
    // should call validateCircuit directly or use exportCircuitWithDiagnostics.
    const first = errors.find(e => e.severity === 'error');
    throw first;
  }

  const ir = fromCircuit(circuitJSON, { synthesisSafe: opts.synthesisSafe });
  ir.name = opts.topName;

  // Build the header so it can include a tri-state preservation warning
  // when the user opted out of synthesisSafe.
  let extraHeader = '';
  if (opts.synthesisSafe === false && Array.isArray(ir._lowerTriStateDiagnostics)) {
    const warn = ir._lowerTriStateDiagnostics.find(d => d.kind === 'tristate-preserved');
    if (warn) extraHeader = `\nWARNING: ${warn.message}`;
  }
  const headerText = buildHeaderText(opts);
  const finalHeader = headerText && extraHeader
    ? headerText + extraHeader
    : (headerText || (extraHeader ? extraHeader.trim() : null));

  return toVerilog(ir, {
    header: !!opts.header || !!extraHeader,
    headerText: finalHeader,
  });
}

// Power-user entry: returns Verilog + diagnostic list + IR, never throws.
export function exportCircuitWithDiagnostics(circuitJSON, options = {}) {
  const opts = { header: true, timestamp: false, ...options };
  const errors = validateCircuit(circuitJSON, { hasTranslator });
  const ir = fromCircuit(circuitJSON);
  ir.name = opts.topName || 'top';
  const verilog = toVerilog(ir, {
    header: !!opts.header,
    headerText: buildHeaderText(opts),
  });
  return { verilog, errors, ir };
}
